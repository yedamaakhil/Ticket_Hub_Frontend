// Function to add a new movie to dummyShowsData
export const addMovieToDummyData = (newMovie) => {
    // Generate a new unique ID (using timestamp to ensure uniqueness)
    const newId = Date.now();
    
    // Find the max existing numeric id to ensure we don't duplicate
    const maxId = Math.max(...dummyShowsData.map(m => parseInt(m.id)), 100000);
    const finalId = Math.max(newId, maxId + 1);
    
    const movieWithId = {
        ...newMovie,
        _id: finalId.toString(),
        id: finalId,
        vote_count: newMovie.vote_count || 0,
        created_at: new Date().toISOString()
    };
    
    // Add to the beginning of the array so new movies appear first
    dummyShowsData.unshift(movieWithId);
    
    // Log for debugging
    console.log(`Movie "${newMovie.title}" added successfully! Total movies: ${dummyShowsData.length}`);
    
    return movieWithId;
};

